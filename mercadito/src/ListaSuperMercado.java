    /**
     * Este metodo sirve para llenar la lista de productos
     * del usuario con lo datos que metio en la lista que se
     * le muestra
     */
    public void llenarLista() {
        ListaDE tListaProductos = Mercadito.listaProductosUsuario;
        for (int i = 0; i < tListaProductos.contarNodos(); i++) {
            Producto tProducot = (Producto) tListaProductos.get(i).getiVal();
            campoMandado.setModel(listaMandado);
            listaMandado.addElement(tProducot.getNombre());
        }
    }
    /**
     * Este metodo es para guardar en la computadora
     * la lista de productos y anexarla con el usuario
     */
    public void llenarConProductosActuales() {
        int tama単o = campoMandado.getLastVisibleIndex();
        ListaDE tListaProductos = new ListaDE();
        for (int i = 0; i <= tama単o; i++) {
            Producto tProducto = new Producto((String) listaMandado.getElementAt(i));
            Nodo<Producto> tNodo = new Nodo(tProducto);
            tListaProductos.agregarNodo(tNodo);
        }
        Mercadito.listaProductosUsuario = tListaProductos;
        User tUser = (User)(Mercadito.listaUsuarios.get(Mercadito.posicionUsuario).getiVal());
        tUser.setListaProductos(Mercadito.listaProductosUsuario);
        try {
            FileOutputStream archivoSalidaUno = new FileOutputStream(Mercadito.nombreListaU);
            ObjectOutputStream salidaDeDato = new ObjectOutputStream(archivoSalidaUno);
            salidaDeDato.writeObject(Mercadito.listaUsuarios);
            salidaDeDato.close();
        } catch (IOException e) {
        }
    }

